package courses;

public class Testswitchcase {

	public static void main(String[] args) {
		int day = 6 ; 
		switch (day) 
		{
		case 1 :
			System.out.println ("Sunday is the first day ");
			
			break ; 

		case 2 :
			System.out.println ("Monday is the 2nd day ");
			break ; 
		case 3 :
			System.out.println ("Tuesday is the 3rd day ");
			
			break ; 
			
		case 4 :
			System.out.println ("Wednesday is the 4th day ");
			break ; 
		case 5 :
			System.out.println ("Thursday is the 5th day ");
			break ; 
		case 6 :
			System.out.println ("Friday is the 6th day ");
			break ; 
		case 7 :
			System.out.println ("Saturday is the 7th day ");
			break ; 
			
			default :
				
				System.out.println ("No valid value  ");
				
				break ; 



		}

	}

}
